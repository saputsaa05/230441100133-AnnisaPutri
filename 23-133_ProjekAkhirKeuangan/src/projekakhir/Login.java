    private void btn_LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_LoginActionPerformed
        String username = tf_Username.getText();
        String password = tf_Password.getText();

        if (username.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Username wajib di isi !!");
            tf_Username.requestFocus();
            return;
        }

        if (password.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Password wajib di isi !!");
            tf_Password.requestFocus();
            return;
        }

        // Verifikasi login dari database
        try (Connection conn = Koneksi.getConnection()) {
            String sql = "SELECT * FROM users WHERE username = ? AND password = ?";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, username);
            pst.setString(2, password);
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                // Login berhasil, ambil userId
                int userId = rs.getInt("user_id");
                JOptionPane.showMessageDialog(null, "Login berhasil!");

                // Buka HomePage dengan userId
                new HomePage(userId).setVisible(true);
                this.dispose();
            } else {
                // Login gagal
                JOptionPane.showMessageDialog(null, "Username atau Password tidak sesuai");
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Kesalahan koneksi: " + e.getMessage());
        }
    }//GEN-LAST:event_btn_LoginActionPerformed

